# Area of a triangle and rectangle without using if-else

base = float(input("Enter base of the triangle: "))
height = float(input("Enter height of the triangle: "))

triangle_area = 0.5 * base * height      ###formula

print("Area of the triangle is:", triangle_area)

length = float(input("Enter length of the rectangle: "))
width = float(input("Enter width of the rectangle: "))

rectangle_area = length * width       ###formula

print("Area of the rectangle is:", rectangle_area)